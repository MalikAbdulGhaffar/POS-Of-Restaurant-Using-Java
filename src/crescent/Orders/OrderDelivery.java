    public void addtables(String Name,int Qty ,Double Price){
    
        DefaultTableModel ot =  (DefaultTableModel) orderDeliveryTable.getModel();
        
       DecimalFormat df = new DecimalFormat("00.00") ;       
       double totPrice = Price * Double.valueOf(Qty) ;
       
       String TotalPrice = df.format(totPrice);
        
        // product allready add chk
        
        for (int row = 0; row < orderDeliveryTable.getRowCount(); row++) {
            
            if (Name == orderDeliveryTable.getValueAt(row, 0)) {
                
                ot.removeRow(orderDeliveryTable.convertRowIndexToModel(row));
                
            } 
        }
        
        
        Vector v = new Vector();
        
        v.add(Name);
        v.add(Qty);
        v.add(TotalPrice); // total price
        
        ot.addRow(v);
        // print
    
    }
