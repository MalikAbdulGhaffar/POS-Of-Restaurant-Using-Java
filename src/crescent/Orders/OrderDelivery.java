    private void takeOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_takeOrderActionPerformed
       
    try {
        DbConnection db = new DbConnection();
        db.OpenConnection();
        
        DefaultTableModel df = (DefaultTableModel) orderDeliveryTable.getModel();
        String query ;
        for (int i = 0; i < orderDeliveryTable.getRowCount(); i++) {
                
                orderDeliveryTable.setValueAt("Taken", i, 4);
            
                OrderId = df.getValueAt(i, 0).toString();
                String Name = df.getValueAt(i, 1).toString();
                String Qty = df.getValueAt(i, 2).toString();
                String Price = df.getValueAt(i, 3).toString();
                
                try{
                
                    String Status = df.getValueAt(i, 4).toString();
                    query = "INSERT INTO orders(OrderID, Name, Quantity, Price, Status) VALUES('"+OrderId+"','"+Name+"', '"+Qty+"', '"+Price+"','"+Status+"')";
                    db.InsertUpdateDelete(query);
                   
                }
                catch(Exception e){
                    JOptionPane.showMessageDialog(null, "No Status");
                }
                
        }
         //ADDING ORDER TOTAL PRICE,CASH RECEIVED,CASH CHANGE IN ANOTHER TABLE
                 query = "INSERT INTO OrderDetails(OrderID,TotalPrice,CashReceived,CashChange,Type,Date) VALUES("
                         + "'"+OrderId+"',"
                         + "'"+(int)MAIN.tot+"',"
                         + "'"+(int)MAIN.paid+"',"
                         + "'"+(int)MAIN.balance+"',"
                         + "'"+"Delivery"+"',"
                         + "'"+new java.util.Date()+"')";
                 db.InsertUpdateDelete(query);
       
    } catch (SQLException ex) {
        Logger.getLogger(OrderDelivery.class.getName()).log(Level.SEVERE, null, ex);
    }
      
       b.append("\nOrder Has Been Taken!");
    }//GEN-LAST:event_takeOrderActionPerformed

