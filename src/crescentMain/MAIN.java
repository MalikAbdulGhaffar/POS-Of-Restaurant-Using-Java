    public void addtables(String Name,int Qty ,Double Price){
    
    try {
        
        DbConnection db = new DbConnection();
        db.OpenConnection();
        dt = (DefaultTableModel) orderTable.getModel();
        
        DecimalFormat df = new DecimalFormat("00.00") ;
        double totPrice = Price * Double.valueOf(Qty) ;
        
        String TotalPrice = df.format(totPrice);
        
        // product allready add chk
        
        for (int row = 0; row < orderTable.getRowCount(); row++) {
            
            if (Name == orderTable.getValueAt(row, 0)) {
                
                dt.removeRow(orderTable.convertRowIndexToModel(row));
                ot.removeRow(orderD.orderDeliveryTable.convertRowIndexToModel(row));
            } 
        }
       
        v = new Vector();
        vorder = new Vector();
        
        if(!v.contains(Name)){
            v.add(Name);
            v.add(Qty);
            v.add(TotalPrice); // total price
            vorder.add("");
            vorder.add(Name);
            vorder.add(Qty);
            vorder.add(TotalPrice);
            
        }
        dt.addRow(v);
        ot.addRow(vorder);
        
    } catch (SQLException ex) {
        java.util.logging.Logger.getLogger(MAIN.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
    }
    
    }
